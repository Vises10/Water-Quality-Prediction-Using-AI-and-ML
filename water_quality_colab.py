import pandas as pd
import numpy as np
import matplotlib.pyplot as plt

from sklearn.model_selection import train_test_split
from sklearn.ensemble import RandomForestClassifier
from sklearn.svm import SVC
from sklearn.pipeline import make_pipeline
from sklearn.preprocessing import StandardScaler
from sklearn.metrics import accuracy_score, precision_score, f1_score, classification_report

"""### Upload your dataset"""

from google.colab import files
uploaded = files.upload()
filename = list(uploaded.keys())[0]
df = pd.read_csv(filename)
df.head()

"""### First look at the data"""

df.info()
df.describe().T

"""### Cleaning the data"""

df_clean = df.copy()
df_clean['Dissolved - Min'] = pd.to_numeric(df_clean['Dissolved - Min'], errors='ignore')
df_clean.fillna(df_clean.mean(numeric_only=True), inplace=True)
df_clean.fillna('Unknown', inplace=True)
df_clean.head()

"""### Creating a simple water-quality label"""

df_clean['Water_Quality'] = ((df_clean['pH - Min'] >= 6.5) & (df_clean['pH - Max'] <= 8.5)).astype(int)
df_clean['Water_Quality'].value_counts()

"""### Preparing data for the model"""

features = df_clean.select_dtypes(include=['number']).drop(columns=['Water_Quality'], errors='ignore')
target = df_clean['Water_Quality']

X_train, X_test, y_train, y_test = train_test_split(features, target, test_size=0.25, random_state=42)

"""### Training the model Random Forest"""

model = RandomForestClassifier()
model.fit(X_train, y_train)
pred = model.predict(X_test)

acc = accuracy_score(y_test, pred)
prec = precision_score(y_test, pred)
f1 = f1_score(y_test, pred)

acc, prec, f1

"""### Model report"""

print(classification_report(y_test, pred))

"""# Training the model SVM"""

svm_model = make_pipeline(StandardScaler(), SVC(kernel='rbf', random_state=42))
svm_model.fit(X_train, y_train)
svm_pred = svm_model.predict(X_test)

svm_acc = accuracy_score(y_test, svm_pred)
svm_prec = precision_score(y_test, svm_pred, zero_division=0)
svm_f1 = f1_score(y_test, svm_pred, zero_division=0)

print("SVM (RBF)")
print("Accuracy:", svm_acc)
print("Precision:", svm_prec)
print("F1 Score:", svm_f1)
print(classification_report(y_test, svm_pred, zero_division=0))

